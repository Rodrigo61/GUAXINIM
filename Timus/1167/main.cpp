int N, K;
vi horses;
vector<vll> memo;

ll solve(int i, int stables) {

    deb("i = ", i);
    deb("stables = ", stables);

    if (stables == 1) {

        int zero_count = 0, one_count = 0;
        for (; i < N; i++) {
            if (horses[i] == 0) {
                ++zero_count;
            } else {
                ++one_count;
            }
        }

        return zero_count * one_count;
    } else if (stables < 1) {
        return 99999999;
    }

    ll &res = memo[i][stables];

    if (res != -1 ) { return res; }

    res = 99999999;
    int zero_count = 0, one_count = 0;
    for (size_t t = 0; t <= N - i - stables + 1; t++) {

        if (horses[i + t] == 0) {
            ++zero_count;
        } else {
            ++one_count;
        }

        res = min(res, solve(i + t + 1, stables - 1) + (zero_count * one_count));
    }

    return res;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    cin >> N >> K;
    horses.resize(N);

    for (size_t i = 0; i < N; i++) {
        cin >> horses[i];
    }

    memo.resize(N, vll(K + 1, -1));
    printf("%lld\n", solve(0, K));

    return 0;
}
