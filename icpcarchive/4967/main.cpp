ll INF = 99999999999;

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int R;
    int cases_count = 0;

    while (cin >> R, R) {

        vector<vi> grid(R, vi(3));

        for (size_t i = 0; i < R; i++) {
            cin >> grid[i][0] >> grid[i][1] >> grid[i][2];
        }


        long long dp[R][3];
        memset(dp,0, sizeof(dp));

        dp[R-1][1] = grid[R-1][1];
        dp[R-1][2] = INF;
        dp[R-1][0] = grid[R-1][0] + grid[R-1][1];

        for (int i = R-2; i >= 0; i--) {
            for (int j = 2; j >= 0; j--) {
                dp[i][j] = grid[i][j];

                ll bottom_left = INF, bottom_middle = INF, bottom_right = INF, right = INF;

                if (j - 1 >= 0) {
                    bottom_left = dp[i + 1][j - 1];
                }
                if (j + 1 < 3) {
                    bottom_right = dp[i + 1][j + 1];
                    right = dp[i][j + 1];
                }

                bottom_middle = dp[i + 1][j];

                deb("i = ", i, " j = ", j);
                deb("bottom_left = ", bottom_left);
                deb("bottom_middle = ", bottom_middle);
                deb("bottom_right = ", bottom_right);
                deb("right = ", right);

                dp[i][j] += min({bottom_left, bottom_middle, bottom_right, right});
            }
        }

        cout << ++cases_count << ". ";
        cout << dp[0][1] << endl;

    }

    return 0;
}
