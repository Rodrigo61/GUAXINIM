#include<bits/stdc++.h>
#define MAXN 1000000
#define SQN 1000

using namespace std;
int isprime[MAXN+1];

void crivo()
{
	int i;
	isprime[0] = 1;
	isprime[1] = 1;
	for(i = 4; i <= MAXN; i+= 2)
		isprime[i] = 1;
	for(i = 3; i <= SQN; i += 2)
	{
		if(!isprime[i])
		{
			for(int j = i*i; j <= MAXN; j += i)
				isprime[j] = 1;
		}
	}
}


int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

	ll n;
	cin >> n;

	crivo();
	ll k = 0;
	for (int i = 0; i < MAXN && i < n; i++)
	{
		if (!isprime[i] && n % i == 0) {
			while (n % i == 0) {
				n /= i;
			}
			++k;
		}	
	}
	
	// Se n > 1 existe um e apenas um primo maior que 10^6 divisor de n.
	// Nao podem haver dois, pois senao os dois seriam fatores de n e isso
	// implicaria n > 10^12.
	if (n > 1) ++k;
	
	printf("%lld\n", (1 << k) - 1 - k);

    return 0;
}
