int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int V, E;
    int n_case = 0;

    while (cin >> V >> E) {

        vector<vi> vet_adj(V);

        for (int i = 0; i < E; ++i) {
            int u, v;
            cin >> u >>v ;

            --u, --v;

            vet_adj[u].pb(v);
            vet_adj[v].pb(u);
        }


        vi vertex_color(V, -1);
        bool possible = true;

        for (int i = 0; i < V; ++i) {

            if (vertex_color[i] == -1) {

                vertex_color[i] = 1;
                queue<int> q;
                q.push(i);


                while (!q.empty()) {

                    int u = q.front();
                    q.pop();

                    for (auto &v : vet_adj[u]) {

                        if (vertex_color[v] == vertex_color[u]) {

                            possible = false;
                            break;

                        } else if (vertex_color[v] == -1) {

                            vertex_color[v] = !(bool)(vertex_color[u]);
                            q.push(v);
                        }
                    }

                    if (!possible) { break; }

                }
            }

            if (!possible) { break; }
        }


        printf("Instancia %d\n", ++n_case);
        if (possible) {
            printf("sim\n\n");
        } else {
            printf("nao\n\n");
        }
    }

    return 0;
}
