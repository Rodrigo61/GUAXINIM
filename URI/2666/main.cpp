int ans;
vi visited;
vi imposto;
vector<vii> vet_adj;
int V, C;


int dfs(int i) {

    visited[i] = 1;


    int total = imposto[i];
    for (auto &pv : vet_adj[i]) {

        if (!visited[pv.first]) {
            int subp_total = dfs(pv.first);
            total += subp_total;

            ans += ((subp_total + C - 1)/C) * pv.second;
        }
    }

    return total;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);


    cin >> V >> C;


    imposto.resize(V);
    int not_zero_count = 0;
    for (int i = 0; i < V; ++i) {
        cin >> imposto[i];

        if (i != 0 && imposto[i]) {
            ++not_zero_count;
        }
    }
    imposto[0] = 0;


    vet_adj.resize(V);
    for (int i = 0; i < V - 1; ++i) {

        int u, v, u2v;
        cin >> u >> v >> u2v;

        --u, --v;

        vet_adj[u].pb(mp(v, u2v));
        vet_adj[v].pb(mp(u, u2v));

    }

    ans = 0;
    visited.assign(V, 0);
    dfs(0);

    printf("%d\n", 2 * ans);
    return 0;
}
