int V, E;
int inf = 9999999;
vi best_dist;
vector<list<int>> vet_adj;

void bfs(int root) {

    queue<int> q;
    q.push(root);

    while (!q.empty()) {
        int u = q.front();
        q.pop();

        for (auto &v : vet_adj[u]) {
            if (best_dist[u] + 1 < best_dist[v]) {
                best_dist[v] = best_dist[u] + 1;
                q.push(v);
            }
        }
    }
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);


    int H;

    cin >> V >> H >> E;

    best_dist.resize(V, inf);

    int u;
    for (size_t i = 0; i < H; i++) {
        cin >> u;
        best_dist[u] = 0;
    }

    vet_adj.resize(V);

    int v;
    for (size_t i = 0; i < E; i++) {
        cin >> u >> v;

        vet_adj[u].push_back(v);
        vet_adj[v].push_back(u);
    }


    for (int i = 0; i < V; ++i) {
        if (best_dist[i] == 0) {
            bfs(i);
        }
    }

    int maxi = 0;
    int maxi_id = 0;
    for (int i = 0; i < V; ++i) {
        if (best_dist[i] > maxi) {
            maxi = best_dist[i];
            maxi_id = i;
        }
    }

    cout << maxi_id << endl;
    return 0;
}
