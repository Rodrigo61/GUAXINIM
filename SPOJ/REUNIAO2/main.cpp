#define INF 1000000000

int V, E;
vector<vi> adj_matrix;


void floyd () {

    for (int k = 0; k < V; k++)
        for (int i = 0; i < V; i++)
            for (int j = 0; j < V; j++)
                adj_matrix[i][j] = min(adj_matrix[i][j], adj_matrix[i][k] + adj_matrix[k][j]);
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    cin >> V >> E;

    adj_matrix.resize(V, vi(V, INF));
    for (int i = 0; i < V; ++i) {
        adj_matrix[i][i] = 0;
    }


    int u, v, u2v;
    for (int i = 0; i < E; ++i) {
        cin >> u >> v >> u2v;

        if (u2v < adj_matrix[u][v]) {
            adj_matrix[u][v] = u2v;
            adj_matrix[v][u] = u2v;
        }
    }

    floyd();


    int min_maxi = INF;
    for (int i = 0; i < V; ++i) {
        int curr_max = *max_element(all(adj_matrix[i]));

        min_maxi = min(min_maxi, curr_max);
    }


    printf("%d\n", min_maxi);
    return 0;
}
