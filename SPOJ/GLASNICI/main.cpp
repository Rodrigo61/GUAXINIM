#define eps 0.000001

double K;
int n_msg;
vector<double> msg_dist;


bool possible(double M) {

    double pos_last_man = msg_dist[0] + M;

	for (int i = 1; i < n_msg; i++) {
		if (msg_dist[i] - M > pos_last_man + K)
			return false;

        pos_last_man = min(pos_last_man + K, msg_dist[i] + M);
	}
	return true;
}

double solve() {
    double upper = 1000000000;
    double lower = 0;
    double mid;

    while (abs(upper - lower) > eps) {
        mid = lower + (upper - lower) / 2;

        deb("upper = ", upper);
        deb("lower = ", lower);
        deb("mid = ", mid);

        if (possible(mid)) {
            deb("possible");
            upper = mid;
        } else {
            deb("impossible");
            lower = mid + 0.0001;
        }
    }

    return lower;
}

