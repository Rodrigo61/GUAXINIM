#define LEFT 1
#define RIGHT 2
#define UP 3
#define DOWN 4


int R, C;
vector<vi> mat;
int maxi_min;
vector<vi> memo;

void solve(int i, int j, int mini, int dist, int origin){

    mini = min(mini, mat[i][j] - dist);

    if(mini <= maxi_min) return;
    if(mini <= memo[i][j]) return;

    memo[i][j] = max(mini, memo[i][j]);


    if(i == R-1 && j == C-1){
        maxi_min = max(maxi_min, mini);
        return;
    }

    if(j-1 >= 0 && origin != LEFT){
        solve(i, j-1, mini, dist+1, RIGHT);
    }
    if(j+1 < C && origin != RIGHT){
        solve(i, j+1, mini, dist+1, LEFT);
    }
    if(i-1 >= 0 && origin != DOWN){
        solve(i-1, j, mini, dist+1, UP);
    }
    if(i+1 < R && origin != UP){
        solve(i+1, j, mini, dist+1, DOWN);
    }


}

int main(){

    scanf("%d%d", &R, &C);

    mat.resize(R, vi(C));
    for (size_t i = 0; i < R; i++) {
        for (size_t j = 0; j < C; j++) {
            scanf("%d", &mat[i][j]);
        }
    }

    memo.resize(R, vi(C, -99999));

    maxi_min = -1;
    solve(0, 0, mat[0][0], 0, 5);

    if(maxi_min < 0){
        printf("-1\n");
    }else{
        printf("%d\n", maxi_min-1);
    }

    return 0;
}
