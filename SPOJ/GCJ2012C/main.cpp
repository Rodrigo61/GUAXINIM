int power[] = {1, 10, 100, 1000, 10000, 100000, 1000000, 10000000};

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int T;
    cin >> T;
    int n_cases = 0;
    int A, B;


    while(T--) {

        cin >> A >> B;

        int count = 0;

        for (int i = A + 1; i <= B; ++i) {

            int n_digits = 0;
            int aux = i;
            while (aux) {
                ++n_digits;
                aux /= 10;
            }

            int rota;
            int j = 1;
            while (j < n_digits) {

                int offset = power[j];
//                deb("offset = ", offset);
                int suffix = i % offset;
//                deb("suffix = ", suffix);
                int prefix = i / offset;
//                deb("prefix = ", prefix);

                suffix *= power[n_digits - j];
                rota = suffix + prefix;

                ++j;

//                deb("rota = ", rota);
                if (rota == i) break;

                if (rota < i && rota >= A) {
                    ++count;
                }
            }
        }


        printf("Case #%d: %d\n", ++n_cases, count);
    }

    return 0;
}
