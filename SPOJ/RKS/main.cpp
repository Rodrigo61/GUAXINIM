int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int N, C;

    cin >> N >> C;
    map<int, int> freq;
    vi vet_nums;
    int num;

    for (int i = 0; i < N; ++i) {
        cin >> num;
        vet_nums.push_back(num);
        if(freq.find(num) != freq.end()){
            ++freq[num];
        }else{
            freq[num] = 1;
        }
    }

    print_vector_debug(vet_nums);

    vector<pii> vet;
    for(auto &p : freq){
        vet.push_back(mp(p.second, p.first));
    }

    sort(all(vet), [=](pii &p1, pii &p2){
        if(p1.first > p2.first) return true;
        if(p1.first < p2.first) return false;

        for(auto &i : vet_nums){
            if(i == p2.second) return false;
            if(i == p1.second) return true;
        }
    });

    for(auto &p: vet){
        for (int i = 0; i < p.first; ++i) {
            cout << p.second << space;
        }
    }
    cout << endl;

    return 0;
}