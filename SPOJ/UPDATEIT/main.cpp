vi BIT;

void update(int idx, int V) {

    idx++;

    while (idx < BIT.size()) {

        BIT[idx] += V;
        idx += (idx & (-idx));

    }
}

void update(int L, int R, int V) {

    update(L, V);
    update(R + 1, -V);
}


int sum(int idx) {

    if (idx < 0) return 0;

    idx++;
    int res = 0;

    while (idx > 0) {

        res += BIT[idx];
        idx -= (idx & (-idx));

    }

    return res;
}

