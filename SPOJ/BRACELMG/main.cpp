int main() {

    int casos;
    cin >> casos;

    for (int w = 0; w < casos; ++w) {

        string seq;
        string bra;
        string bra_extend;
        bool encontrou = false;

        cin >> seq >> bra;
        
        /*String extendida*/
        for (int i = 0; i < 100; ++i) {
            bra_extend.append(bra.begin(), bra.end());
        }

        /*Busca horaria*/
        if(bra_extend.find(seq) != string::npos){
            encontrou = true;
        }

        /*Busca antihoraria*/
        reverse(all(bra_extend));
        if(bra_extend.find(seq) != string::npos){
            encontrou = true;
        }

        deb("w = ", w);
        deb("casos = ", casos);

        if(encontrou){
            cout << "S" << endl;
        }else{
            cout << "N" << endl;
        }


    }

    return 0;
}