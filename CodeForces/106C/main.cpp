int n, m;
vi b, c, d, a;
vector<vi> memo;

int solve(int dough, int idx) {

    if (idx == m + 1) {
        return 0;
    }

    int &res = memo[dough][idx];
    if (res != -1) return res;

    res = 0;

    int n_items = 0;
    while (1) {

        if (n_items * c[idx] <= dough && n_items * b[idx] <= a[idx]) {

            res = max(res, solve(dough - n_items * c[idx], idx + 1) + n_items * d[idx]);
            ++n_items;

        } else {
            break;
        }

    }

//    deb("res = ", res);
    return res;
}

