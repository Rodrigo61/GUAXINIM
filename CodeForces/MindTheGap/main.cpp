pii get_hour (ll minutes) {

    pii res;

    res.first = minutes/60;
    res.second = minutes % 60;

    return res;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int n_lands, min_interval;

    cin >> n_lands >> min_interval;

    int hour, minute;
    ll last_minute = 0;
    ll current_minute;

    bool found = false;
    bool first = true;

    for (size_t i = 0; i < n_lands; i++) {
        cin >> hour >> minute;
        current_minute = hour * 60 + minute;

        if (first && current_minute >= min_interval + 1) {
            auto res = get_hour(0);
            cout << res.first << " " << res.second << endl;
            found = true;
            break;
        }

        first = false;


        if (current_minute - last_minute >= min_interval * 2 + 1) {
            auto res = get_hour(last_minute + min_interval);
            cout << res.first << " " << res.second << endl;
            found = true;
            break;
        }

        last_minute = current_minute + 1;
    }


    if (!found) {
        auto res = get_hour(last_minute + min_interval);
        cout << res.first << " " << res.second << endl;
    }

    return 0;
}
