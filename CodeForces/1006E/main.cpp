    cin >> N >> Q;

    vet_adj.resize(N);

    for (int i = 0; i < N - 1; ++i) {
        int parent;
        cin >> parent;
        vet_adj[parent - 1].insert(i + 1);

    }


    curr_time = 0;
    times.resize(N);
    dfs(0);
