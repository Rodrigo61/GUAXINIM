int N, Q;
vector<set<int>> vet_adj;
vi range;
vii times;
int curr_time;

void dfs(int u) {

    times[u].first = curr_time;
    range.pb(u + 1);

    for (auto &v : vet_adj[u]) {
        ++curr_time;
        dfs(v);
    }

    times[u].second = curr_time;

}

