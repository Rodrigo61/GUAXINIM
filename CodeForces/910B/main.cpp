int n, a, b;

set<pii> options;
vector<vi> memo;

vector<vi> memoprecalc;

void precalc(int not_used, int x, int y) {

    if (x != 0 || y != 0) {

        if (memoprecalc[x][y]) return;

        options.insert(mp(x, y));
        memoprecalc[x][y] = 1;
    }

    if (not_used >= a) {
        precalc(not_used - a, x + 1, y);
    }

    if (not_used >= b) {
        precalc(not_used - b, x, y + 1);
    }
}


int solve(int A, int B) {

    if (A == 0 && B == 0) {
        return 0;
    }


    int &res = memo[A][B];

    if (res != -1) return res;

    res = 99999999;

    for (auto &opt : options) {

        if (A - opt.first >= 0 && B - opt.second >= 0) {
            res = min(res, solve(A - opt.first, B - opt.second) + 1);
        }
    }

    return res;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    cin >> n >> a >> b;

    memoprecalc.resize(1001, vi(1001, 0));
    precalc(n, 0, 0);


    memo.resize(5, vi(3, -1));
    printf("%d\n", solve(4, 2));

    return 0;
}
