int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;

    vll values(n);
    for (int i = 0; i < n; ++i) {
        cin >> values[i];
    }

    vll acu_sum(n);
    acu_sum[0] = values[0];
    for (int i = 1; i < n; ++i) {
        acu_sum[i] = values[i] + acu_sum[i - 1];
    }
    
    
    map<ll, int> map_suffix;
    ll last_suffix = values[n - 1];
    map_suffix[last_suffix] = n - 1;
    for (int i = n - 2; i >= 0; --i) {
        last_suffix = last_suffix + values[i];
        
        if (map_suffix.find(last_suffix) == map_suffix.end()) {
            map_suffix[last_suffix] = i;
        }
    }


    ll max_found = -1;
    for (int i = 0; i < n - 1; ++i) {


        if (map_suffix.find(acu_sum[i]) != map_suffix.end()) {

            int s2 = map_suffix[acu_sum[i]];

            if (i < s2) {
                max_found = max(max_found, acu_sum[i]);
            }
        }
    }


    if (max_found != -1) {
        printf("%lld\n", max_found);
    } else {
        printf("0\n");
    }
    return 0;
}
