int main(){
    ios_base::sync_with_stdio(false);

    int n_cases;
    int n_sf;
    int maxi;
    int sf;
    vi vet_seq(1000001);
    vector<int>::iterator last_removed_idx;

    cin >> n_cases;
    for (int w = 0; w < n_cases; ++w) {
        cin >> n_sf;
        set<int> set_sf;
        maxi = 0;
        last_removed_idx = vet_seq.begin();

        for (int i = 0; i < n_sf; ++i) {
            cin >> sf;
            vet_seq[i] = sf;

            deb("sf = ", sf);
            if(set_sf.find(sf) != set_sf.end()){
                auto sf_idx = find(last_removed_idx, vet_seq.end(), sf);

                for (auto j = sf_idx; j != last_removed_idx-1; --j) {
                    set_sf.erase(*j);
                }

                last_removed_idx = sf_idx+1;
            }

            set_sf.insert(sf);

            deb("(int)set_sf.size() = ", (int)set_sf.size());
            maxi = max(maxi, (int)set_sf.size());
        }

        cout << maxi << endl;

    }



    return 0;
}