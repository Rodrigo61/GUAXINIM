void aux_dfs(int root, const vector<vi>& vet_adj, vi& visited, vi& vertices_ordered){

    visited[root] = 1;

    for(auto &v : vet_adj[root]){
        if(!visited[v])
            aux_dfs(v, vet_adj, visited, vertices_ordered);
    }

    vertices_ordered.push_back(root);
}

vi topological_sort(int V, const vector<vi>& vet_adj){

    vi visited(V, 0);
    vi vertices_ordered;

    for (size_t i = 0; i < V; i++) {
        if(!visited[i]){
            aux_dfs(i, vet_adj, visited, vertices_ordered);
        }
    }

    reverse(all(vertices_ordered));

    return vertices_ordered;
}

