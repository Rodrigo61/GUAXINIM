int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);


    while(true){

        map<string, int> projects;
        map<string, string> students;
        string proj, stud;

        while(true){

            if(cin.peek() == '0'){
                return 0;
            }

            getline(cin, proj);
            projects[proj] = 0;
            deb("proj = ", proj);

            do{
                if(cin.peek() < 'a' || cin.peek() > 'z')
                    break;

                getline(cin, stud);
                deb("stud = ", stud);

                if(students.find(stud) != students.end()){

                    if(students[stud] == proj) continue;
                    if(students[stud] == "CANELADO@") continue;

                    projects[students[stud]]--;
                    students[stud] = "CANELADO@";
                }else{

                    students[stud] = proj;
                    projects[proj]++;
                }

            }while(true);

            if(cin.peek() == '1'){
                getline(cin, proj);
                break;
            }
        }


        vector<pair<string, int>> ans;
        ans.reserve(projects.size());
        for(auto &p : projects){
            ans.push_back(p);
        }

        sort(all(ans), [](pair<string, int> &p1, pair<string, int> &p2){
            if(p1.second > p2.second) return true;
            if(p1.second < p2.second) return false;

            return p1.first < p2.first;
        });

        for(auto &p : ans){
            cout << p.first << " " << p.second << endl;
        }

    }

    return 0;
}