int n;
vi L, R;
vector<vi> memo;


int dist(int i, int j) {
    return abs(i - j);
}

int solve(int i, int j) {

    int &res = memo[i][j];

    if (res != - 1) return res;

    if (i == n - 1) {

        res = dist(j, L[n - 1]) + dist(L[n - 1], n - 1);
        return res;
    }

    int l = solve(i + 1, L[i]) + dist(j, R[i]) + dist(L[i], R[i]);
    int r = solve(i + 1, R[i]) + dist(j, L[i]) + dist(L[i], R[i]);

    res = min(l, r) + 1;


    return res;

}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);


    while (cin >> n, n) {

        L.clear();
        L.resize(n);
        R.clear();
        R.resize(n);

        for (int i = 0; i < n; ++i) {
            cin >> L[i] >> R[i];
            --L[i];
            --R[i];
        }

        memo.clear();
        memo.resize(n + 5, vi(n + 5, -1));
        printf("%d\n", solve(0, 0));

    }
    return 0;
}

