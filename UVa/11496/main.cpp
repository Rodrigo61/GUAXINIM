int main() {


    int N;

    cin >> N;

    while(N != 0){

        vector<int> vet_samples(N+2);

        for (int i = 0; i < N; ++i) {
            cin >> vet_samples[i+1];
        }
        vet_samples[0] = vet_samples[N];
        vet_samples[N+1] = vet_samples[1];

        int picos = 0;
        for (int i = 1; i < N+1; ++i) {
            if(vet_samples[i] > vet_samples[i-1] && vet_samples[i] > vet_samples[i+1]){
                ++picos;
            }else if(vet_samples[i] < vet_samples[i-1] && vet_samples[i] < vet_samples[i+1]){
                ++picos;
            }
        }

        cout << picos << endl;
        cin >> N;
    }
    return 0;
}