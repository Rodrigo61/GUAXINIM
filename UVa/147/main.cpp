vi currency = {10000, 5000, 2000, 1000, 500, 200, 100, 50, 20, 10, 5};



int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    double aux;
    ll amount;

    while (cin >> aux) {
        amount = aux * 100;

        while (amount % 5 != 0) {
            amount++;
        }

        if (amount == 0) { break; }

        deb("amount = ", amount);

        ll dp[amount + 1];
        memset(dp, 0, sizeof(dp));

        dp[0] = 1;
        for (auto &cur : currency) {
            for (int i = 1; i < amount + 1; i++) {
                if (i - cur >= 0) {
                    dp[i] += dp[i - cur];
                }
            }
        }


        string spaces;
        if (amount >= 10000)     { spaces = ""; }
        else if (amount >= 1000) { spaces = " "; }
        else                     { spaces = "  "; }

        string left_zero = "";
        if (amount % 100 < 10) {
            left_zero = "0";
        }

        cout << spaces << amount / 100 << "." << left_zero << amount % 100;

        string ans = to_string(dp[amount]);

        for (int i = ans.size(); i < 17; ++i) { cout << " "; }

        cout << ans << endl;
    }

    return 0;
}
