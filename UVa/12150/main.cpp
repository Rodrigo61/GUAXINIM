int main() {

    int N, P, C;

    cin >> N;

    while(N != 0){

        vector<int> grid(N,0);

        deb(1);
        bool grid_possivel = true;
        int soma = 0;
        for(int i = 0; i < N; ++i){
            cin >> C >> P;

            deb(2);
            int prev_pos = i + P;
            soma += P;

            deb("prev_pos = ", prev_pos, "  i = ", i);
            if(prev_pos < 0 || prev_pos >= N || grid[prev_pos] != 0){
                grid_possivel = false;
            }else{
                grid[prev_pos] = C;
            }
            deb(3);
        }

        if(!grid_possivel || soma != 0){
            cout << "-1" << endl;
        }else{
            printVector(grid);
        }

        cin >> N;
    }

    return 0;
}