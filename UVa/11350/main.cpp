pii get_frac(pii left_frac, pii right_frac, pii curr_frac, string &path, int pos){

    if(pos == path.size()){
        return curr_frac;
    }

    deb("left :", left_frac.first, ", ", left_frac.second);
    deb("curr_frac :", curr_frac.first, ", ", curr_frac.second);
    deb("right :", right_frac.first, ", ", right_frac.second);

    if(path[pos] == 'L'){

        return get_frac(left_frac, curr_frac,
                        mp(left_frac.first+curr_frac.first, left_frac.second+curr_frac.second),
                        path, pos+1);
    }else{
        return get_frac(curr_frac, right_frac,
                        mp(right_frac.first+curr_frac.first, right_frac.second+curr_frac.second),
                        path, pos+1);
    }
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int n_cases;
    cin >> n_cases;

    for (int w = 0; w < n_cases; ++w) {

        string path;
        cin >> path;

        pii frac = get_frac(mp(0,1), mp(1,0), mp(1,1), path, 0);
        cout << frac.first << "/" << frac.second << endl;
    }

    return 0;
}