    while(cin >> max_time >> n_tracks){

        w.clear();
        w.resize(n_tracks);

        for (int i = 0; i < n_tracks; ++i) {
            cin >> w[i];
        }

        print_vector_debug(w);
        pair<int, vi> res = solve(0, max_time);
        reverse(all(res.second));
        for(auto &i : res.second){
            cout << i << " ";
        }
        cout << "sum:" << res.first << endl;
    }
    return 0;
