int main(){
    ios_base::sync_with_stdio(false);

    int n_cases;
    cin >> n_cases;
    int board[100];
    int n_players, n_ladders, n_dies;
    int player_pos[1000000];

    for (int w = 0; w < n_cases; ++w) {

        memset(board, 0, 100*sizeof(int));

        cin >> n_players >> n_ladders >> n_dies;

        for (int i = 0; i < n_players; ++i) {
            player_pos[i] = 0;
        }

        for (int i = 0; i < n_ladders; ++i) {
            int bottom, top;
            cin >> bottom >> top;

            board[bottom-1] = top-1;
        }


        int turn = 0;
        int die;

        while(n_dies > 0){

            cin >> die;
            --n_dies;

            player_pos[turn] += die;

            if(player_pos[turn] >= 99) {
                player_pos[turn] = 99;
                break;
            }

            if(board[player_pos[turn]] != 0){
                player_pos[turn] = board[player_pos[turn]];
                deb("ladder| player_pos[turn] = ", player_pos[turn]);
                if(player_pos[turn] >= 99) {
                    player_pos[turn] = 99;
                    goto fim;
                }
            }

            if(n_dies == 0){
                break;
            }


            turn = (turn+1) % n_players;

        }

        fim:
        for (int i = 0; i < n_dies; ++i) {
            cin >> die;
        }

        for (int i = 0; i < n_players; ++i) {
            cout << "Position of player " << i+1 << " is " << player_pos[i]+1 << "." << endl;
        }

    }

    return 0;
}