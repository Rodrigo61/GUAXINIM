int main() {

    int casos;

    cin >> casos;

    for(int i = 0; i < casos; ++i){
        int N;
        cin >> N;
        int P;
        cin >> P;

        vector<int> visited(N+1, 0);

        for(int j = 0; j < P; ++j){
            int H;
            cin >> H;

            for(int k = H; k <= N; k += H){

                deb("k = ", k);

                bool feriado = false;

                if(k % 7 == 0){
                    feriado = true;
                }

                for (int l = 6; l <= k; l+=7) {
                    if(k == l){
                        feriado = true;
                    }
                }

                if(!feriado){
                    visited[k] = 1;
                }
            }
        }


        print_vector_debug(visited);


        int acu = 0;
        for(auto v : visited){
            acu += v;
        }

        cout << acu << endl;
    }


    return 0;
}