vi cuts(55);
vector<vi> memo(1010, vi(1010));
vi::iterator cuts_end;

int solve(int i, int j) {

    if (i == j) {
        return 0;
    }

    int &res = memo[i][j];

    if (res != -1) {
        return res;
    }

    res = 0;
    for (auto it = upper_bound(cuts.begin(), cuts_end, i); it != cuts_end && *it <= j; ++it){
        int cut = *it;

        if (res == 0) {
            res = 99999;
        }

        res = min(res, solve(i, cut - 1) + solve(cut, j) + (j - i + 1));
    }

    return res;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int length;

    while (cin >> length, length) {

        int n_cuts;
        cin >> n_cuts;

        for (size_t i = 0; i < n_cuts; i++) {
            cin >> cuts[i];
        }

        cuts_end = cuts.begin() + n_cuts;

        for (size_t i = 0; i < length; i++) {
            for (size_t j = 0; j < length; j++) {
                memo[i][j] = -1;
            }
        }

        cout << "The minimum cutting is " << solve(0, length - 1) << "." << endl;
    }


    return 0;
}
