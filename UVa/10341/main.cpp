double p,q,r,s,t,u;

double eval(double x){
    double sinx = sin(x);
    double cosx = cos(x);
    double tanx = tan(x);
    double expo = pow(M_E, -x);

    return p*expo + q*sinx + r*cosx + s*tanx + t*pow(x,2) + u;
}


double solve(){

    double upper = 1.0;
    double lower = 0.0;

    double mid = (upper+lower)/2.0;

    while(abs(upper - lower) > 0.0000001){
        mid = (upper+lower)/2.0;
        double mid_eval = eval(mid);
        double lower_eval = eval(lower);

        deb("mid = ", mid);
        deb("lower = ", lower);
        if(mid_eval*lower_eval <= 0){
            upper = mid;
        }else{
            lower = mid;
        }
    }

    return mid;
}

