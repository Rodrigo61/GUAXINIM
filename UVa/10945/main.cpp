void format(string &str){

    int pos;
    while(pos = str.find("!"), pos != string::npos){
        str.erase(pos, 1);
    }
    while(pos = str.find("?"), pos != string::npos){
        str.erase(pos, 1);
    }
    while(pos = str.find("."), pos != string::npos){
        str.erase(pos, 1);
    }
    while(pos = str.find(","), pos != string::npos){
        str.erase(pos, 1);
    }
    while(pos = str.find(" "), pos != string::npos){
        str.erase(pos, 1);
    }


    for(auto &c : str){
        c = toupper(c);
    }
}

