int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    string str;
    cin >> str;

    vector<vi> vet_states_rep(128);

    int id = 0;
    for(auto &c : str){
        vet_states_rep[c].push_back(id);
        id += 1;
    }


    int n_query;
    cin >> n_query;

    for (size_t i = 0; i < n_query; i++) {
        string query;
        cin >> query;

        if(!vet_states_rep[query[0]].empty()){

            int first_idx = vet_states_rep[query[0]][0];
            int last_idx = first_idx;

            bool match = true;

            for (size_t i = 1; i < query.size(); i++) {
                auto bit = upper_bound(all(vet_states_rep[query[i]]), last_idx);

                if(bit == vet_states_rep[query[i]].end()){
                    match = false;
                    break;
                }

                last_idx = *bit;
            }

            if(match){
                cout << "Matched " << first_idx << " " << last_idx << endl;
            }else{
                cout << "Not matched" << endl;
            }
        }else{
                cout << "Not matched" << endl;
        }
    }
    return 0;
}
