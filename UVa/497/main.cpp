int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int n_cases;
    cin >> n_cases;
    cin.ignore();

    string line;
    getline(cin, line);

    for (size_t q = 0; q < n_cases; q++) {

        vi vet_heigth;

        while(getline(cin, line), !line.empty()){

            vet_heigth.push_back(stoi(line));
        }

        int sz = vet_heigth.size();

        int dp[sz];
        int parent[sz];

        dp[sz - 1] = 1;
        parent[sz - 1] = -1;

        int maxi_ind = sz - 1;
        int maxi_val = 1;

        for (int i = sz - 2; i >= 0; i--) {

            dp[i] = 0;
            parent[i] = -1;
            for (int k = i + 1; k < sz; k++) {
                if (vet_heigth[k] >= vet_heigth[i] && dp[k] >= dp[i]) {
                    dp[i] = dp[k];
                    parent[i] = k;
                }
            }

            dp[i] += 1;

            if (dp[i] >= maxi_val) {
                maxi_val = dp[i];
                maxi_ind = i;
            }
        }


        cout << "Max hits: " << maxi_val << endl;
        while(maxi_ind != -1) {
            cout << vet_heigth[maxi_ind] << endl;
            maxi_ind = parent[maxi_ind];
        }

        if (q != n_cases - 1) {
            cout << endl;
        }


    }

    return 0;
}
