int main(){
    ios_base::sync_with_stdio(false);

    int k;
    int x_sol[1000000];
    int y_sol[1000000];

    while(scanf("%d", &k) == 1){

        int y = k + 1;
        int x;
        int max_y = 2*k;
        int sol_count = 0;

        for (int i = y; i <= max_y; ++i) {

            /*deb("i = ", i);
            deb("i-k = ", i-k);*/
            if(k*i % (i-k) == 0){
                y_sol[sol_count] = i;
                x_sol[sol_count] = (k*i)/(i-k);
                sol_count++;
            }

        }

        cout << sol_count << endl;
        for (int i = 0; i < sol_count; ++i) {
            x = x_sol[i];
            y = y_sol[i];
            cout << "1/" << k << " = 1/" <<x << " + 1/" << y << endl;
        }
    }

    return 0;
}