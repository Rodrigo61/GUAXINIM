long long memo[210][12][22];
int M, D;
int N, Q;
vi A;

long long solve(int idx, int left, int sum){

    if(left == 0){
        return sum == 0;
    }
    if(idx == N) return 0;

    if(memo[idx][left][sum] != -1) return memo[idx][left][sum];

    long long &res = memo[idx][left][sum];

    int temp_sum = (sum + (A[idx] % D))%D;
    temp_sum = (temp_sum + D)%D; // evitando resto negativo

    res = solve(idx+1, left-1, temp_sum) + solve(idx+1, left, sum);

    deb("res = ", res);
    return res;
}

