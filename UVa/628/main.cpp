int n_words;
string rule;
string curr_password;
vector<string> words;



void gen(int idx_rule, int pass_sz){

    if(idx_rule == rule.size()){
        for (int i = 0; i < pass_sz; ++i) {
            cout << curr_password[i];
        }
        cout << endl;
        return;
    }
    
    if(rule[idx_rule] == '0'){
        /*digit*/
        for (int i = '0'; i <= '9'; ++i) {
            curr_password[pass_sz] = i;
            gen(idx_rule+1, pass_sz+1);
        }
    }else{
        /*word*/
        for(auto &w : words){
            curr_password.insert(pass_sz, w);
            gen(idx_rule+1, pass_sz+w.size());
        }
    }
}


