vector<string> vet_left(3), vet_right(3), vet_action(3);

bool is_possible(int weight, char target){
    int left_weight, right_weight;

    for (int i = 0; i < 3; ++i) {
        left_weight = 0;
        for(auto &c : vet_left[i]){
            if(c != target){
                left_weight += 1;
            }else{
                left_weight += weight;
            }
        }

        right_weight = 0;
        for(auto &c : vet_right[i]){
            if(c != target){
                right_weight += 1;
            }else{
                right_weight += weight;
            }
        }

        if(vet_action[i] == "up" && right_weight >= left_weight)
            return false;
        if(vet_action[i] == "down" && right_weight <= left_weight)
            return false;
        if(vet_action[i] == "even" && right_weight != left_weight)
            return false;
    }

    return true;
}

