int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int n_cases;
    cin >> n_cases;
    int case_count = 0;

    for (int w = 0; w < n_cases; ++w) {

        vi fa,fb,fc;
        fa.reserve(10001), fb.reserve(10001), fc.reserve(10001);
        vi problems(10001, 1);

        for (int k = 2; k <= 4; ++k) {
            int p_count;
            cin >> p_count;

            int problem;
            for (int i = 0; i < p_count; ++i) {
                cin >> problem;

                problems[problem] *= k;

            }
        }

        //print_vector_debug(problems);

        for (int i = 0; i < 10001; ++i) {
            if(problems[i] == 2){
                fa.push_back(i);
            }else if(problems[i] == 3){
                fb.push_back(i);
            }else if(problems[i] == 4){
                fc.push_back(i);
            }
        }


        vi aux = {fa.size(), fb.size(), fc.size()};

        int max_size = *max_element(all(aux));


        cout << "Case #" << ++case_count << ":" << endl;
        if(fa.size() == max_size){
            cout << 1 << space << max_size;
            for(auto e: fa){
                cout << space << e;
            }cout << endl;
        }
        if(fb.size() == max_size){
            cout << 2 << space << max_size;
            for(auto e: fb){
                cout << space << e;
            }cout << endl;
        }
        if(fc.size() == max_size){
            cout << 3 << space << max_size;
            for(auto e: fc){
                cout << space << e;
            }cout << endl;
        }
    }



    return 0;
}