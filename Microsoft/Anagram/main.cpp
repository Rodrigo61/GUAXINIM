void read_str(string &str){

    char c;
    str = "";

    while(cin >> c, c!= '"');

    while(cin >> c, c!= '"'){
        str += c;
    }
}

void format(string &str){

    string aux = str;
    str = "";
    for(auto &c : aux){
        if(c == ' ') continue;
        str += tolower(c);
    }

    sort(all(str));
}



