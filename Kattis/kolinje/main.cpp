struct Person{
    double init;
    double prop;
};

int N;
vector<Person> vet_person;

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    cin >> N;

    double init, prop;
    double C = 0;

    vet_person.resize(N);
    for (int i = 0; i < N; ++i) {
        cin >> init >> prop;
        vet_person[i].init = init;
        vet_person[i].prop = prop;

        C += prop;
    }

    deb("C = ", C);

    Person last_person = vet_person[0];
    double mini = 0, maxi = 10000000;
    for (int i = 1; i < N; ++i) {
        double left = (last_person.init - vet_person[i].init) * C;
        double right = vet_person[i].prop - last_person.prop;

        if (right == 0 && left < 0) {
            maxi = -1;
            break;
        }

        if (right < 0) {
            mini = max(mini, left/right);
        } else if (right > 0){
            maxi = min(maxi, left/right);
        }

        last_person = vet_person[i];
    }


    if (maxi >= mini) {
        printf("%lf\n", (maxi+mini)/2);
        //printf("%lf\n", maxi);
    } else {
        printf("-1\n");
    }

    return 0;
}
