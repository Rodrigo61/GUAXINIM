int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int window_sz;
    cin >> window_sz;

    int next_years;
    cin >> next_years;

    int n_elem = window_sz + next_years - 1;

    if (window_sz > n_elem) {
        printf("unknown\n");
        return 0;
    }

    vii vet(n_elem);
    for (size_t i = 0; i < n_elem; i++) {
        cin >> vet[i].first >> vet[i].second;
    }

    int str_target = vet[0].second;

    sort(all(vet));
    int first_year = vet[0].first;

    priority_queue<int, vi> pq;

    for (size_t i = 0; i < window_sz; i++) {
        pq.push(vet[i].second);
    }


    bool found = false;

    for (int i = 0; pq.size() >= window_sz; i++) {
        int u = pq.top();
        pq.pop();

        if (u == str_target) {
            found = true;
            printf("%d\n", first_year + i);
            break;
        }

        if (window_sz + i >= n_elem) { break; }

        pq.push(vet[window_sz + i].second);

    }

    if (!found) {
        printf("unknown\n");
    }

    return 0;
}
