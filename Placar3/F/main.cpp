double P, T, Q;
vector<double> probs;

double solve(int round){

    if(round == 1){
        probs[0] = 1.0 - Q/(T+Q);
        probs[1] = Q/(T+Q);

        print_vector_debug(probs);
        return Q/(T+Q);
    }

    solve(round-1);

    probs[1] = probs[0] * (Q)/(T + Q - round + 1) + probs[1]*(1- (Q - 1)/(T + Q - round + 1));
    probs[0] = probs[0]* (1- (Q /(T + Q - round + 1)));

    print_vector_debug(probs);

    return probs[1];

}


